package week1.day2;

public class LearnForLoop {

	// starting number=1
	// ending number=5
	// increased by 1

	public static void main(String[] args) {
		// To print numbers 1 to 5

		// 1 2 3 4 5
		for (int i = 1; i <= 5; i++) {
			System.out.println(i);

		}
		
		
		System.out.println("------------------");
        //  5   4    3    2     1
		//To print numbers from 5 to 1
		//starting number=5
		//ending number=1
		//Decreased by 1
		
		//5   4   3    2   1
		for(int i=5;i>=1;i--) {      
			System.out.println(i);
		}
		
	}

}
